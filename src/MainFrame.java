    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildStudentComboBoxes()
    {
        ArrayList<StudentEntry> students = StudentQueries.getAllstudents();
        SSCSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        SDSSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        SDCSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));
        ADSSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(students.toArray()));

    }
    
    public void rebuildCourseComboBoxes()
    {
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        SSCSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        ADCSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        ADCLSSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
    }
    public void rebuildCourseComboBoxes(StudentEntry Student)
    {   if (Student.studentID != null || currentSemester != null){
            ArrayList<CourseEntry> remainingcourses = ScheduleQueries.getremainingcourses(Student.studentID, currentSemester);
            SDCSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(remainingcourses.toArray()));
        
        }
        else
        {
            ArrayList<String> emptylist = new ArrayList<String>();
            SDCSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(emptylist.toArray()));
        
        }
    }

