    private void ADSDropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADSDropStudentButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student = (StudentEntry) ADSSelectStudentComboBox.getSelectedItem();
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        StudentEntry checkforbidden = null;
        
        StudentQueries.dropStudent(student.studentID);
        ADSTextArea.append(student + " (" + student.studentID + ") has been dropped from students list. \n\n" );
        
        for (String semester:semesters)
        {
            ADSTextArea.append("For Semester: " + semester + "\n");
            ArrayList<CourseEntry> courses = ScheduleQueries.getremainingcourses(student.studentID, semester);
            
            for (CourseEntry course:courses)
            {
                checkforbidden = ScheduleQueries.dropStudentScheduleByCourse(semester, student.studentID, course.courseCode);
                if (checkforbidden.firstName == "NN" && checkforbidden.lastName == "NN" && checkforbidden.studentID == "NN")
                {
                    
                }
                else if (checkforbidden.firstName == "FF" && checkforbidden.lastName == "FF" && checkforbidden.studentID == "FF")
                {
                    ADSTextArea.append(student + " (" + student.studentID + ") has been dropped from waitlist for " + course + "\n");
                }
                
                else if(checkforbidden.firstName == "00" && checkforbidden.lastName == "00" && checkforbidden.studentID == "00")
                {
                    ADSTextArea.append(student + " (" + student.studentID + ") has been dropped from " + course + "\n");
                }
                
                else
                {   ADSTextArea.append(student + " (" + student.studentID + ") has been dropped from " + course + "\n");
                    ADSTextArea.append(checkforbidden + " (" + checkforbidden.studentID + ") has been scheduled for " + course + "\n");
                }
            }
            ADSTextArea.append("Semester changes displayed\n\n");
        }
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_ADSDropStudentButtonActionPerformed

    private void ADCDropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADCDropCourseButtonActionPerformed
        // TODO add your handling code here:
        String semester = currentSemester;
        CourseEntry course = (CourseEntry) ADCSelectCourseComboBox.getSelectedItem();
        ArrayList<StudentEntry> scheduledstudents = ScheduleQueries.getScheduledStudentsByCourse(semester, course.courseCode);
        ArrayList<StudentEntry> waitlistedstudents = ScheduleQueries.getWaitlistedStudentsByCourse(semester, course.courseCode);
        
        ScheduleQueries.dropScheduleByCourse(semester, course.courseCode);
        CourseQueries.dropCourse(semester, course.courseCode);
        
        ADCTextArea.append("Scheduled students dropped from the course:\n");
        for (StudentEntry scheduledstudent:scheduledstudents)
        {
            ADCTextArea.append(scheduledstudent + "\n");
        }
        
        ADCTextArea.append("Waitlisted students dropped from the course:\n");
        for (StudentEntry waitlistedstudent:waitlistedstudents)
        {
            ADCTextArea.append(waitlistedstudent + "\n");
        }
        
        rebuildCourseComboBoxes();
        StudentEntry studentDC = (StudentEntry) SDCSelectStudentComboBox.getSelectedItem();
        if (studentDC != null){
            rebuildCourseComboBoxes(studentDC);
        }
        
        else
        {
            ArrayList<String> emptylist = new ArrayList<String>();
            SDCSelectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(emptylist.toArray()));
        }
    }//GEN-LAST:event_ADCDropCourseButtonActionPerformed
    
